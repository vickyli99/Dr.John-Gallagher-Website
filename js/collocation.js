function myFunction() {
    var image = document.getElementById("test");
    var word_index = document.getElementById("validationCustom03").selectedIndex;
    var word = document.getElementById("validationCustom03").options[word_index].text;
    if (word === 'Write') {
        var category_index = document.getElementById("validationCustom04").selectedIndex;
        var category = document.getElementById("validationCustom04").options[category_index].text;
        if (category === 'Yearly') {
            var sw_index = document.getElementById("validationCustom05").selectedIndex;
            var sw = document.getElementById("validationCustom05").options[sw_index].text;
            image.src = write_yearly_links[sw];
        }
        else {
            var sw_index = document.getElementById("validationCustom05").selectedIndex;
            var sw = document.getElementById("validationCustom05").options[sw_index].text;
            image.src = write_journal_links[sw];
        }
    }
    else if (word === 'Transfer') {
        var category_index = document.getElementById("validationCustom04").selectedIndex;
        var category = document.getElementById("validationCustom04").options[category_index].text;
        if (category === 'Yearly') {
            var sw_index = document.getElementById("validationCustom05").selectedIndex;
            var sw = document.getElementById("validationCustom05").options[sw_index].text;
            image.src = transfer_yearly_links[sw];
        }
        else {
            var sw_index = document.getElementById("validationCustom05").selectedIndex;
            var sw = document.getElementById("validationCustom05").options[sw_index].text;
            image.src = transfer_journal_links[sw];
        }
    }
}